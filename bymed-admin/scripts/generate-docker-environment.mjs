/**
 * Writes `src/environments/environment.docker.ts` from ADMIN_API_PUBLIC_URL
 * (scheme + host + port, no path). Used by the admin Docker image build.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outFile = path.join(root, "src/environments/environment.docker.ts");

const origin = (process.env.ADMIN_API_PUBLIC_URL ?? "http://localhost:8080")
  .trim()
  .replace(/\/+$/, "");
const apiBaseUrl = `${origin}/api/v1`;

const content = `import { AppEnvironment } from '../app/core/config/app-environment';

/** Generated by scripts/generate-docker-environment.mjs — do not edit by hand. */
export const environment: AppEnvironment = {
  production: true,
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
  enableVerboseLogging: false,
};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, "utf8");
console.log("Wrote", path.relative(root, outFile), "apiBaseUrl =", apiBaseUrl);
